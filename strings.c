#include <stdio.h>
#include <ctype.h>
#include <string.h>

/*===========================================================================
  trimright
===========================================================================*/
void trimright (char *s)
  {
  int l = strlen (s);
  while (l > 0)
    {
    l--;
    if (s[l] == 10) s[l] = 0; 
    else if (s[l] == 13) s[l] = 0; 
    else return;
    }
  }

/*===========================================================================
  strlwr 
===========================================================================*/
void strlwr (char *s)
  {
  while (*s)
    {
    *s = tolower (*s);
    s++;
    }
  }



